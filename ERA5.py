def download_era5_data(output_filename, max_retries=3):
    """Download ERA5 data with robust error handling."""

    # Initialize CDS client
    client = cdsapi.Client()

    global pressurelevel

    # Define request parameters - try GRIB first as it's more common for ERA5
    dataset = "reanalysis-era5-pressure-levels"
    variable = ["vorticity", "u_component_of_wind", "v_component_of_wind"] # "u_component_of_wind", "v_component_of_wind"
    pressurelevel = ["500"]
    year = ["2023"]
    month = ["01"]
    day = ["16"]
    time_str = ["12:00"]

    def requestfunction(dataset, variable, pressurelevel, year, month, day, time):
        request = {
            "variable": variable,
            "year": year,
            "month": month,
            "format": "grib"  # GRIB is the native ERA5 format
        }

        if dataset == "reanalysis-era5-land-monthly-means":
            request["product_type"] = "monthly_averaged_reanalysis"
            request["time"] = time

        elif dataset == "reanalysis-era5-land":
            request["product_type"] = "reanalysis"
            request["day"] = day
            request["time"] = time

        elif dataset == "reanalysis-era5-pressure-levels":
            request["product_type"] = "reanalysis"
            request["pressure_level"] = pressurelevel
            request["day"] = day
            request["time"] = time

        return request

    request = requestfunction(dataset, variable, pressurelevel, year, month, day, time_str)

    # Retry loop for robust downloading
    for attempt in range(max_retries):
        try:
            print(f"Attempt {attempt + 1}: Downloading data...")

            # Remove existing file if it exists
            if os.path.exists(output_filename):
                os.remove(output_filename)

            # Download data
            client.retrieve(dataset, request, output_filename)

            # Verify file integrity
            if os.path.exists(output_filename) and os.path.getsize(output_filename) > 1000:
                print(f"✓ Successfully downloaded {os.path.getsize(output_filename)} bytes")

                # Verify file is readable
                try:
                    with open(output_filename, 'rb') as f:
                        header = f.read(4)
                        print(f"File header: {header}")
                    return request  # Return request for use in plotting
                except Exception as e:
                    print(f"File verification failed: {e}")
                    continue
            else:
                print(f"✗ Download failed or file too small")
                time.sleep(10)  # Wait longer between retries

        except Exception as e:
            print(f"✗ Error on attempt {attempt + 1}: {e}")
            time.sleep(10)

    return None, # Return None if all attempts failed

def diagnose_file(filename):
    """Diagnose file format and content."""
    print(f"\n=== File Diagnostics for {filename} ===")

    if not os.path.exists(filename):
        print("File does not exist!")
        return False

    file_size = os.path.getsize(filename)
    print(f"File size: {file_size} bytes")

    if file_size == 0:
        print("File is empty!")
        return False

    # Read first few bytes to identify format
    try:
        with open(filename, 'rb') as f:
            header = f.read(16)
            print(f"First 16 bytes: {header}")
            print(f"As hex: {header.hex()}")

            # Check for common file signatures
            if header.startswith(b'GRIB'):
                print("✓ Looks like a GRIB file")
                return 'grib'
            elif header.startswith(b'\x89HDF') or header.startswith(b'CDF'):
                print("✓ Looks like a NetCDF/HDF file")
                return 'netcdf'
            elif header.startswith(b'PK\x03\x04') or header.startswith(b'PK\x05\x06'):
                print("✓ Looks like a ZIP file")
                return 'zip'
            else:
                print("✗ Unknown file format")
                # Try to read as text to see if it's an error message
                f.seek(0)
                try:
                    text_content = f.read(500).decode('utf-8', errors='ignore')
                    if 'error' in text_content.lower() or 'exception' in text_content.lower():
                        print("File appears to contain an error message:")
                        print(text_content[:200])
                except:
                    pass
                return False

    except Exception as e:
        print(f"Error reading file: {e}")
        return False

def extract_from_zip(zip_filename, extract_dir):
    """Extract files from ZIP archive and return the data file path."""
    try:
        with zipfile.ZipFile(zip_filename, 'r') as zip_ref:
            # List contents
            file_list = zip_ref.namelist()
            print(f"ZIP contents: {file_list}")

            # Extract all files
            zip_ref.extractall(extract_dir)

            # Look for data files (GRIB or NetCDF)
            for filename in file_list:
                file_path = os.path.join(extract_dir, filename)
                if filename.lower().endswith(('.grib', '.grb', '.nc', '.netcdf')):
                    print(f"Found data file: {filename}")
                    return file_path

            # If no obvious data file, try the largest file
            largest_file = max(file_list, key=lambda x: os.path.getsize(os.path.join(extract_dir, x)))
            print(f"Using largest file: {largest_file}")
            return os.path.join(extract_dir, largest_file)

    except Exception as e:
        print(f"Error extracting ZIP: {e}")
        return None

def find_best_variable(ds, preferred_variables=None):
    """Find the best variable to use from the dataset."""
    available_vars = list(ds.data_vars.keys())
    print(f"Available variables: {available_vars}")

    if not available_vars:
        raise ValueError("No data variables found in dataset")

    # If preferred variables are specified, try to find them
    if preferred_variables:
        for pref_var in preferred_variables:
            for var in available_vars:
                if pref_var.lower() in var.lower() or var.lower() in pref_var.lower():
                    print(f"Found matching variable: '{var}' for preference '{pref_var}'")
                    return var

    # Otherwise, return the first available variable
    selected_var = available_vars[0]
    print(f"Using first available variable: '{selected_var}'")
    return selected_var

