def diagnose_file(filename):
    """Diagnose file format and content."""
    print(f"\n=== File Diagnostics for {filename} ===")

    if not os.path.exists(filename):
        print("File does not exist!")
        return False

    file_size = os.path.getsize(filename)
    print(f"File size: {file_size} bytes")

    if file_size == 0:
        print("File is empty!")
        return False

    # Read first few bytes to identify format
    try:
        with open(filename, 'rb') as f:
            header = f.read(16)
            print(f"First 16 bytes: {header}")
            print(f"As hex: {header.hex()}")

            # Check for common file signatures
            if header.startswith(b'GRIB'):
                print("✓ Looks like a GRIB file")
                return 'grib'
            elif header.startswith(b'\x89HDF') or header.startswith(b'CDF'):
                print("✓ Looks like a NetCDF/HDF file")
                return 'netcdf'
            elif header.startswith(b'PK\x03\x04') or header.startswith(b'PK\x05\x06'):
                print("✓ Looks like a ZIP file")
                return 'zip'
            else:
                print("✗ Unknown file format")
                # Try to read as text to see if it's an error message
                f.seek(0)
                try:
                    text_content = f.read(500).decode('utf-8', errors='ignore')
                    if 'error' in text_content.lower() or 'exception' in text_content.lower():
                        print("File appears to contain an error message:")
                        print(text_content[:200])
                except:
                    pass
                return False

    except Exception as e:
        print(f"Error reading file: {e}")
        return False

def extract_from_zip(zip_filename, extract_dir):
    """Extract files from ZIP archive and return the data file path."""
    try:
        with zipfile.ZipFile(zip_filename, 'r') as zip_ref:
            # List contents
            file_list = zip_ref.namelist()
            print(f"ZIP contents: {file_list}")

            # Extract all files
            zip_ref.extractall(extract_dir)

            # Look for data files (GRIB or NetCDF)
            for filename in file_list:
                file_path = os.path.join(extract_dir, filename)
                if filename.lower().endswith(('.grib', '.grb', '.nc', '.netcdf')):
                    print(f"Found data file: {filename}")
                    return file_path

            # If no obvious data file, try the largest file
            largest_file = max(file_list, key=lambda x: os.path.getsize(os.path.join(extract_dir, x)))
            print(f"Using largest file: {largest_file}")
            return os.path.join(extract_dir, largest_file)

    except Exception as e:
        print(f"Error extracting ZIP: {e}")
        return None

def find_best_variable(ds, preferred_variables=None):
    """Find the best variable to use from the dataset."""
    available_vars = list(ds.data_vars.keys())
    print(f"Available variables: {available_vars}")

    if not available_vars:
        raise ValueError("No data variables found in dataset")

    # If preferred variables are specified, try to find them
    if preferred_variables:
        for pref_var in preferred_variables:
            for var in available_vars:
                if pref_var.lower() in var.lower() or var.lower() in pref_var.lower():
                    print(f"Found matching variable: '{var}' for preference '{pref_var}'")
                    return var

    # Otherwise, return the first available variable
    selected_var = available_vars[0]
    print(f"Using first available variable: '{selected_var}'")
    return selected_var

def process_data(filename, lat_min, lat_max, lon_min, lon_max, request, keep_time=False):
    """Process ERA5 data and create visualization."""

    # First diagnose the file
    file_format = diagnose_file(filename)
    if not file_format:
        return None, None

    # Handle ZIP files
    actual_data_file = filename
    if file_format == 'zip':
        print("Extracting ZIP file...")
        extract_dir = os.path.dirname(filename)
        actual_data_file = extract_from_zip(filename, extract_dir)
        if actual_data_file is None:
            print("Failed to extract data from ZIP file")
            return None, None

        # Re-diagnose the extracted file
        file_format = diagnose_file(actual_data_file)
        if not file_format:
            return None, None

    print("Opening dataset...")

    # Try different engines based on detected format
    if file_format == 'grib':
        engines_to_try = ['cfgrib']
    else:
        engines_to_try = ['netcdf4', 'scipy']

    ds = None
    for engine in engines_to_try:
        try:
            print(f"Trying engine: {engine}")
            ds = xr.open_dataset(actual_data_file, engine=engine, backend_kwargs={'errors': 'ignore'})
            print(f"✓ Successfully opened with {engine} engine")
            break
        except Exception as e:
            print(f"✗ Failed with {engine}: {str(e)[:150]}...")
            continue

    if ds is None:
        print("Engines failed. Trying without specifying engine...")
        try:
            ds = xr.open_dataset(actual_data_file)
        except Exception as e:
            print(f"Final attempt failed: {e}")
            return None, None

    print("Available ds variables:", list(ds.data_vars.keys()))
    print("ds Dataset dimensions:", dict(ds.dims))
    print("ds Dataset coordinates:", list(ds.coords.keys()))

    def windspeedrequest_confirmation(u_data, v_data):
        if 'windspeedrequest' in globals() and windspeedrequest == 'Yes':
            # Compute wind speed
            wind_speed = np.sqrt(u_data**2 + v_data**2)
            ds['wind'] = wind_speed  # Save as new variable
            return ds, wind_speed

    # --- Then optionally build wind speed ---
    if 'u' in ds.data_vars and 'v' in ds.data_vars:
        print("✓ U and V wind components found. Calculating wind speed.")
        u_data = ds['u']
        v_data = ds['v']
        windspeedrequest_confirmation(u_data, v_data)

    elif 'u10' in ds.data_vars and 'v10' in ds.data_vars:
        print("✓ U and V wind components found. Calculating wind speed.")
        u_data = ds['u10']
        v_data = ds['v10']
        windspeedrequest_confirmation(u_data, v_data)

    # Handle time dimension first, then apply regional filtering
    if 'time' in ds.dims:
        if keep_time: # <--- NEW CHECK
            ds_single_time = ds
        else:
            ds_single_time = ds.isel(time=0)
    else:
        ds_single_time = ds

    # Apply regional filtering to the entire dataset
    ds_regional = ds_single_time.where(
        (ds_single_time.latitude >= lat_min) & (ds_single_time.latitude <= lat_max) &
        (ds_single_time.longitude >= lon_min) & (ds_single_time.longitude <= lon_max),
        drop=True
    )

    if 'windspeedrequest' in globals() and windspeedrequest == 'Yes':
        var_name = 'wind'
        dataReady = ds_regional['wind']
        print("var name should be wind")
    else:
        # Get the main data variable from the filtered dataset
        # Find the best variable to use
        var_name = find_best_variable(ds, request.get('variable', []))
        dataReady = ds_regional[var_name]

    return dataReady, ds_regional, var_name

