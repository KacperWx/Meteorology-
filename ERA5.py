def open_dataset(filename):
    """Safely open a GRIB or NetCDF dataset."""
    file_format = diagnose_file(filename)
    if not file_format:
        return None
    actual_data_file = filename
    if file_format == 'zip':
        print("Extracting ZIP file...")
        extract_dir = tempfile.mkdtemp()
        actual_data_file = extract_from_zip(filename, extract_dir)
        if actual_data_file is None:
            print("Failed to extract data from ZIP file")
            return None
        file_format = diagnose_file(actual_data_file)
        if not file_format:
            return None

    ds = None
    engines_to_try = ['cfgrib'] if file_format == 'grib' else ['netcdf4', 'scipy']
    for engine in engines_to_try:
        try:
            print(f"Trying engine: {engine}")
            if engine == 'cfgrib':
                ds = xr.open_dataset(actual_data_file, engine=engine, backend_kwargs={'errors': 'ignore'})
            else:
                ds = xr.open_dataset(actual_data_file, engine=engine)
            print(f"✓ Successfully opened with {engine} engine")
            return ds
        except Exception as e:
            print(f"✗ Failed with {engine}: {str(e)[:150]}...")
            continue

    print("All engines failed. Trying without specifying engine...")
    try:
        ds = xr.open_dataset(actual_data_file)
        return ds
    except Exception as e:
        print(f"Final attempt failed: {e}")
        return None

