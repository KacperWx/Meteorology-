def main(position_lat, position_lon, offset):
    """Main function to orchestrate the data download and visualization."""

    global var_name
    global anomalyrequest # Make sure anomalyrequest is accessible

    # Check for longitude
    if position_lon < 0:
        position_lon = position_lon + 360

    lat_min = position_lat - offset # * 2
    lat_max = position_lat + offset # * 2
    lon_min = position_lon - offset * 1.5
    lon_max = position_lon + offset * 1.5

    print("=== ERA5 Data Analysis ===")
    print(f"Custom region: {lat_min}-{lat_max}, {lon_min}-{lon_max}")

    # Setup API
    if not setup_cds_api():
        return

    # Define output filename
    output_dir = "/content" if os.path.exists("/content") else "."
    output_filename = os.path.join(output_dir, "era5_data.grib")

    # Download the main data
    request = download_era5_data(os.path.basename(output_filename))
    if request is None:
        print("Failed to download main data after multiple attempts.")
        return

    # Process the main dataset
    dataReady, ds_regional, var_name = process_data(os.path.basename(output_filename), lat_min, lat_max, lon_min, lon_max, request)

    if dataReady is None:
        print("Failed to process main data.")
        return

    # Calculate the anomaly if requested
    if anomalyrequest == 'Yes':
        climo_file = os.path.join(output_dir, "era5_climodata.grib")
        # Download the climatology data
        climorequest = download_era5_data(os.path.basename(climo_file))
        if climorequest is None:
            print("Failed to download climatology data after multiple attempts.")
            return

        # Corrected line: Unpack 3 values, even if the third (climo_var_name) isn't used
        climoData, climo_ds, climo_var_name = process_data(os.path.basename(climo_file), lat_min, lat_max, lon_min, lon_max, climorequest, keep_time=True)

        if climoData is None:
            print("Failed to process climatology data.")
            return

        # --- FIX STARTS HERE ---
        # 1. Get the month number from the main request to filter the climatology
        target_month = int(request['month'][0])

        # 2. Select only the month corresponding to the target month from all years, then average over time
        # This filters the 360-month climoData down to the 30 Januarys (for example) and averages them.
        climo_monthly_mean = climoData.sel(time=(climoData.time.dt.month == target_month)).mean(dim='time')

        # 3. Calculate the anomaly (Data - ClimoMean)
        # Use .sel with method='nearest' to align the grids if they differ slightly
        dataReady_anomaly = dataReady.sel(
            latitude=climo_monthly_mean.latitude,
            longitude=climo_monthly_mean.longitude,
            method='nearest'
        ) - climo_monthly_mean

        # Pass the anomaly data to the plotting function
        plot_data(lat_min, lat_max, lon_min, lon_max, dataReady_anomaly, ds_regional, request, var_name)
        # --- FIX ENDS HERE ---
    else:
        # Plot the original data if no anomaly is requested
        plot_data(lat_min, lat_max, lon_min, lon_max, dataReady, ds_regional, request, var_name)

    # Clean up
    dataReady.close()
    ds_regional.close()
    if anomalyrequest == 'Yes':
        climoData.close()
        climo_ds.close()

# Run the analysis with custom boundaries
if __name__ == "__main__":
    # You can customize these boundaries here:
    position_lat = -5
    position_lon = 90
    offset = 20

    main(position_lat, position_lon, offset)

